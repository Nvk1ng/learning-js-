//operador logico and (&&) -> so pode ser true se ambos os operandos forem true
console.log(true && true);
console.log(false && true);

//operador logico or (||) -> pode ser true se ao menos um dos operandos for true
console.log(false || true);
console.log(false || false);
console.log(true || true);

//operador logico not (!) -> inverte o valor logico do operando
console.log(!true);
console.log(!false);
